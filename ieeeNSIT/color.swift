import Foundation
import UIKit


    
public func getColor(red : CGFloat, green : CGFloat, blue : CGFloat) -> UIColor
{
    
    let color = UIColor(red: red/255, green: green/255, blue: blue/255, alpha: 1)
    return color
    
}
    



